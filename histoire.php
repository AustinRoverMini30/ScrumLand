    <section id="fondation">
        <h2>Les Origines : Un Petit Village en Bois</h2>
        <p>
            La ville de <strong>ScrumLand</strong> a été fondée en <strong>1950</strong> à partir d’un 
            <em>modeste village en bois</em>, situé au bord d’un magnifique lac. À cette époque, les habitants 
            vivaient principalement de la pêche, de l’agriculture et de petits commerces locaux.
        </p>
        <p>
            Grâce à sa localisation idéale et à la richesse naturelle de son environnement, le village 
            prospérait lentement mais sûrement, attirant des familles venues chercher tranquillité et 
            simplicité.
        </p>
    </section>

    <!-- Section Guerre Froide -->
    <section id="guerre-froide">
        <h2>La Guerre Froide : Un Tournant Technologique</h2>
        <p>
            Durant la <strong>Guerre Froide</strong>, ScrumLand a connu un développement majeur. Les dirigeants 
            locaux, visionnaires et audacieux, décidèrent d'investir dans la <strong>recherche technologique</strong> 
            pour faire de la ville un centre d'innovation.
        </p>
        <ul>
            <li>Création de <strong>laboratoires</strong> dédiés aux sciences informatiques.</li>
            <li>Accélération du développement des premières <em>méthodes agiles</em>, ancêtres de Scrum.</li>
            <li>Attraction de <strong>scientifiques</strong> et ingénieurs du monde entier pour travailler sur des 
            projets collaboratifs.</li>
        </ul>
        <p>
            C’est durant cette période que la méthodologie <strong>Scrum</strong> a trouvé ses premières applications 
            pratiques dans l’organisation des équipes de recherche, posant ainsi les bases d’une nouvelle 
            façon de travailler.
        </p>
    </section>

    <!-- Section Vers le Futur -->
    <section id="futur">
        <h2>ScrumLand Aujourd'hui : Une Ville Tournée Vers l'Avenir</h2>
        <p>
            Grâce à ses racines solides et son héritage technologique, ScrumLand est aujourd’hui reconnue 
            comme une ville **futuriste** et **écologique**, centrée sur :
        </p>
        <ol>
            <li>Le développement de <strong>technologies innovantes</strong> comme la cybersécurité et l’intelligence artificielle.</li>
            <li>La mise en place d’un système éducatif avec des <em>bourses pour les étudiants en informatique</em>.</li>
            <li>Un engagement écologique fort avec l’entretien du <strong>lac</strong> et la promotion des énergies renouvelables.</li>
        </ol>
        <p>
            ScrumLand reste fidèle à sa devise : "<em>Innover pour le bien-être collectif</em>", et continue 
            d’inspirer des générations de chercheurs et d’entrepreneurs.
        </p>
    </section>

    
